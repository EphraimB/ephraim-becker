          <h2><time itemprop="startDate" datetime="2014-01-01">2013-2014</time></h2>
          <h3 itemprop="name">YALA school in torah v'dath</h3>
          <p itemprop="description">Switched schools to the special needs YALA from the mainstream Darchei. Finally found out that I have autism after my parents have been hiding it from me all these years. It finally explained why I was different from everyone else. It was revealed to me from a paper of other people's diagnosis hanging around. I didn't understand anything about what a real friend was. I had people pretending to be my friend in the mainstream school, Yeshiva Darchei Torah, that I switched from. I thought that friends were just boring people. There was someone else on the autism spectrum that wanted to be my friend (for real). The problem is that he didn't give me space (I have that problem also) and got uncomfortable with him. I was wondering why I was always being lumped together with this person. I didn't know what autism was until a year later. I went to far with what I did by bullying him. I never knew that there was such a thing as people needing space. I feel ashamed of myself for what I did to him. That school, YALA, closed down because of mis-communication from staff. If only the staff could explain to me what was going on and what autism actually is but they didn't. They just treated me and the other person I was bullying as troublemakers.</p>
        </div>
